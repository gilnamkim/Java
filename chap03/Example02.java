package chap03;

public class Example02 {
    public static void main(String[] args) {
        int x = 10, y = 10;

        // < 관계연산자 >
        // 두 피연산자의 크고 작음을 비교하기 위해 사용합니다.
        // 두 피연산자의 관계가 참이면 true를 반환하고 거짓이면 false를 반환합니다.
        // 일반적으로 관계 연산자는 if문, while문 등에서 조건식을 판별하는데 사용합니다. 
        System.out.println(x==y); 
        System.out.println("x와 y는 같습니까? "+(x==y));
    }
}
