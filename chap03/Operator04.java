package chap03;

public class Operator04 {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = 30;

        System.out.println(c);
        System.out.println(c = a);
        System.out.println(b += a);
        System.out.println(b -= a);
        System.out.println(b *= a);
        System.out.println(b /= a);
        System.out.println(b %= a);
    }
}
