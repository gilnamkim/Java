package Chap02;

public class Datatype01 {
    public static void main(String[] args) {
        int a = 10;
        short s = 2;
        byte b = 6;
        long l = 1253621332135L; //int형 범위를 벗어나면 long형 이라는 표시를 해주어야 합니다.
        System.out.println(a);
        System.out.println(s);
        System.out.println(b);
        System.out.println(l);
    }
    
}
