package chap02;

public class Datatype02 {
    public static void main(String[] args) {
        float f = 65.20298f; //float형 4바이트 변수 설정
        double d = 876.765d; //double형 8바이트 변수 설정
        // long형과 마찬가지로 임시저장되는 int형에 벗어나기 때문에 변수형을 붙여줘야한다.
        
        System.out.println(f);
        System.out.println(d);
    }
}
